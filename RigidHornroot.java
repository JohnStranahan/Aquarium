package aquarium;

public class RigidHornroot extends Plants
{
	

		public RigidHornroot()
		{
		
		
		super();

		super.setName("Rigid Hornroot");
		super.setMinCarbonDioxide(6);//ppm
		super.setMaxCarbonDioxide(25);//ppm
		super.setMinAmmonium(0);//ppm
		super.setMaxAmmonium(1);
		super.setMinPh(6.2);//pH
		super.setMaxPh(7.5);//pH
		super.setMinWaterHardness(5);//dKH
		super.setMaxWaterHardness(15);//dKH
		super.setMinTemperature(59);//Fahrenheit
		super.setMaxTemperature(82.4);//Fahrenheit
	
	
}
}

