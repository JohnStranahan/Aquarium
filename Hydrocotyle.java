package aquarium;

public class Hydrocotyle extends Plants 
{
		public Hydrocotyle() {
		
		
		super();

		super.setName("Hydrocotyle");
		super.setMinCarbonDioxide(6);//ppm
		super.setMaxCarbonDioxide(25);//ppm
		super.setMinAmmonium(0);//ppm
		super.setMaxAmmonium(1);
		super.setMinPh(6);//pH
		super.setMaxPh(7.8);//pH
		super.setMinWaterHardness(4);//dKH
		super.setMaxWaterHardness(18);//dKH
		super.setMinTemperature(59);//Fahrenheit
		super.setMaxTemperature(77);//Fahrenheit

	
	
	
	
}
}