package aquarium;

public class WaterWisteria extends Plants 
{
	public WaterWisteria()
	{
	
	
	super();

	super.setName("Water Wisteria");
	super.setMinCarbonDioxide(6);//ppm
	super.setMaxCarbonDioxide(25);//ppm
	super.setMinAmmonium(0);//ppm
	super.setMaxAmmonium(1);
	super.setMinPh(6.8);//pH
	super.setMaxPh(7.5);//pH
	super.setMinWaterHardness(0);//dKH
	super.setMaxWaterHardness(8);//dKH
	super.setMinTemperature(75);//Fahrenheit
	super.setMaxTemperature(82);//Fahrenheit


}
	
	
	
	
	
	
	
	
	
}
