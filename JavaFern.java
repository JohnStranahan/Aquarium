package aquarium;

public class JavaFern extends Plants 
{



	public JavaFern()
	{
	
	
	super();

	super.setName("Java Fern");
	super.setMinCarbonDioxide(8);//ppm
	super.setMaxCarbonDioxide(25);//ppm
	super.setMinAmmonium(0);//ppm
	super.setMaxAmmonium(1);
	super.setMinPh(6);//pH
	super.setMaxPh(8);//pH
	super.setMinWaterHardness(5);//dKH
	super.setMaxWaterHardness(15);//dKH
	super.setMinTemperature(72);//Fahrenheit
	super.setMaxTemperature(82);//Fahrenheit

	
	
}

	
}
