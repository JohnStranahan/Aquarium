package aquarium;

public class Lilaeopsis extends Plants

{


	public Lilaeopsis()
	{
	
	
	super();

	super.setName("Lilaeopsis");
	super.setMinCarbonDioxide(8);//ppm
	super.setMaxCarbonDioxide(25);//ppm
	super.setMinAmmonium(0);//ppm
	super.setMaxAmmonium(1);
	super.setMinPh(6.8);//pH
	super.setMaxPh(7.5);//pH
	super.setMinWaterHardness(5);//dKH
	super.setMaxWaterHardness(15);//dKH
	super.setMinTemperature(70);//Fahrenheit
	super.setMaxTemperature(70-83);//Fahrenheit

	
	}
}
