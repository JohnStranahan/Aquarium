package aquarium;
//prefers soft acidic water
public class AmazonSwordPlant extends Plants
{

	public AmazonSwordPlant() {
		
		
		super();

		super.setName("Amazon Sword Plant");
		super.setMinCarbonDioxide(6);//ppm
		super.setMaxCarbonDioxide(25);//ppm
		super.setMinAmmonium(0);//ppm
		super.setMaxAmmonium(1);//ppm  may delete ammonium and use in environment. ph dependent
		super.setMinPh(6.5);//pH
		super.setMaxPh(7.5);//pH
		super.setMinWaterHardness(3);//dKH
		super.setMaxWaterHardness(8);//dKH
		super.setMinTemperature(72);//Fahrenheit
		super.setMaxTemperature(82);//Fahrenheit

	}

	
	
		
	}

	
	
	
	

